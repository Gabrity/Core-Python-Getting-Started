# integer
print(int(-3.5))
print(int("5"))

# float: double precision number with 53 digits of binary
print(float("5.55"))
print(float("nan"))
print(float("-inf"))

# None ~ null value
a = None
print(a is None)

# bool
bool(0) # false
bool(321) # true
bool("") # false
bool([]) # false
bool("False") # true